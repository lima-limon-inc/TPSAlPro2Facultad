func TestVolumen(t *testing.T) {
	t.Log("Hacemos la prueba de volumen")
	cola := TDACola.CrearColaDinamica[int]()
	for i := 0; i < 10000; i++ {
		cola.Encolar(i)
	}
	for i := 10000; i == 0; i-- {
		require.EqualValues(t, i, cola.EstaVacia())
	}
}
